# 44. Leia o raio de um círculo. Considere π = 3.14159. Calcule e exiba a área do círculo com duas casas decimais.

π = 3.14159
RaioCírculo = 3
ÁreaCírculo = π*RaioCírculo**2


print('Qual a área de um círculo que possui 3 metros de raio?')
print(f'\nRaio do Círculo = {RaioCírculo} m')
print(f'π = {π}')
print(f'Área do Círculo = πr² = 3.14159 * 3² = {ÁreaCírculo} m²')
print(f'\nA área desse círculo é {ÁreaCírculo} m².')