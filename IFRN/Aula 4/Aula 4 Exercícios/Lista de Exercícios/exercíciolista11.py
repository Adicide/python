# 11. Em Python, valores numéricos podem ser usados diretamente em contextos lógicos: 
# 0 é considerado False e qualquer número diferente de zero é considerado True. 
# Qual é o resultado das expressões abaixo?

# bool(F)
# bool(A)

A=4
F=0

bool(F)
bool(A)

print(f'{bool(F)}\n{bool(A)}')

# Resultado: False, True