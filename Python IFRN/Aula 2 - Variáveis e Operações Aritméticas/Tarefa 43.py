# 43. Leia a base e a altura de um retângulo. Calcule e exiba a área do retângulo.

BaseRetângulo = 3
AlturaRetângulo = 4
ÁreaRetângulo = BaseRetângulo*AlturaRetângulo

print('Quantos m² tem um retângulo de 3 metros de base e 4 metros de altura?')

print(f'\nBase do Retângulo = {BaseRetângulo} m')
print(f'Altura do Retângulo = {AlturaRetângulo} m')
print(f'Área do Retângulo = 3 m * 4 m = {ÁreaRetângulo} m²')

print(f'\nEsse retângulo tem {ÁreaRetângulo} m² de área.')